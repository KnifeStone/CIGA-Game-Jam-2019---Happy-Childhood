const {ccclass, property} = cc._decorator;

@ccclass
export default class ItemStage extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null

    stage:number = 0

    initData(stage:number){
        this.stage = stage
        this.label.string = `${stage+1}`
    }

    onClick(){
        cc.game.emit("select-stage",this.stage)
    }

}
